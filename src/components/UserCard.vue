<template>
  <div class="col-6 col-md-3">
    <a href="#">
      <img :src="user.image" width="140px" height="140px" />
    </a>
    <h2>{{user.name}}</h2>
    <span class="badge badge-secondary">追蹤人數：{{user.FollowerCount}}</span>
    <p class="mt-3">
      <button
        v-if="user.isFollowed"
        @click.stop.prevent="unfollow"
        type="button"
        class="btn btn-danger"
      >取消追蹤</button>
      <button v-else @click.stop.prevent="follow" type="button" class="btn btn-primary">追蹤</button>
    </p>
  </div>
</template>

<script>
export default {
  props: {
    initialUser: {
      type: Object,
      required: true
    }
  },
  data() {
    return {
      user: this.initialUser
    };
  },
  methods: {
    follow() {
      return (this.user = {
        ...this.user,
        isFollowed: true,
        FollowerCount: (this.user.FollowerCount += 1)
      });
    },
    unfollow() {
      return (this.user = {
        ...this.user,
        isFollowed: false,
        FollowerCount: (this.user.FollowerCount -= 1)
      });
    }
  }
};
</script>